n1 = int(input('Digite um número '))
n2 = int(input('Digite outro número '))

# Operações:

soma = n1+n2
subtração = n1-n2
multiplicação = n1*n2
divisão = n1//n2

# Resultado:

print('{} + {} = {}'.format(n1, n2, soma))
print('{} - {} = {}'.format(n1, n2, subtração))
print('{} x {} = {}'.format(n1, n2, multiplicação))
print('{} / {} = {}'.format(n1, n2, divisão))